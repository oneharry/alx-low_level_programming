/**
  * add - add values of 2 integers
  * @a: int 1
  * @b: int 2
  * Return: sum of 2 integers
  */
int add(int a, int b)
{
	return (a + b);
}
/**
  * sub - substract value b from a
  * @a: int 1
  * @b: int 2
  * Return: difference of 2 int
  */
int sub(int a, int b)
{
	return (a - b);
}
/**
  * mul - multiplies 2 intf=s
  * @a: int 1
  * @b: int 2
  * Return: products of 2 ints
  */
int mul(int a, int b)
{
	return (a * b);
}
/**
  * div - divides 2 ints
  * @a: int 1
  * @b: int 2
  * Return: int
  */
int div(int a, int b)
{
	return (a / b);
}
/**
  * mod - modulus of 2 int
  * @a: int 1
  * @b: int 2
  * Return: remainder 
  */
int mod(int a, int b)
{
	return (a % b);
}
