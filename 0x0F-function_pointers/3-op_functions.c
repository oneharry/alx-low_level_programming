#include "3-calc.h"

/**
  * op_add - adds two integer
  * @a: first integer
  * @b: second integer
  * Return: sum of integers
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - finds the differenc btw two integer
  * @a: first integer
  * @b: second integer
  * Return: difference of the 2 integers
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - multiply 2 integer
  * @a: first integer
  * @b: second integer
  * Return: multiplication of the 2 integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - divides two integer
  * @a: first integer
  * @b: second integer
  * Return:value of the division of integers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - find modulus of two integer
  * @a: first integer
  * @b: second integer
  * Return: remainder of integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
